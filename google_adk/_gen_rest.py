def nb(cells):
    out = []
    for ct, src in cells:
        if isinstance(src, str): src = [src]
        entry = {"cell_type": ct, "metadata": {}, "source": src}
        if ct == "code": entry["outputs"] = []; entry["execution_count"] = None
        out.append(entry)
    return {"cells": out, "metadata": {"kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"}, "language_info": {"name": "python", "version": "3.12.0"}}, "nbformat": 4, "nbformat_minor": 4}

def save(name, cells):
    with open(os.path.join(BASE, name), 'w', encoding='utf-8') as f:
        json.dump(nb(cells), f, indent=1, ensure_ascii=False)

